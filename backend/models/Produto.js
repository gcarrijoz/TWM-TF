
class Produto {
    constructor(id, nome, descricao, categoria, mensalidade, franquia) {
        this.id = id;
        this.nome = nome;
        this.descricao = descricao;
        this.categoria = categoria;
        this.mensalidade = mensalidade;
        this.franquia = franquia;
    }
}

module.exports = Produto;
